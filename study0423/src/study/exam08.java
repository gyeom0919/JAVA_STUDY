package study;

import java.util.Scanner;

public class exam08 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		
		String yes;
		yes = sc.next();
		
		switch(yes) {
		
		case "부장": 
			System.out.println("상여금 700만원 지급");
			break;
		case "과장":
			System.out.println("상여금 500만원 지급");
			break;
		default:
			System.out.println("상여금 300만원 지금");
		}

	}

}
